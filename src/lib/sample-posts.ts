很多系统最后都会变成一种自我要求：要更自律，要更积极，要更高效。但日记如果也变成这种工具，它会很快让人疲惫。

我更愿意把它看成一种缓慢的认识过程。它不是为了每天都把自己修正得更好，而是让我们逐渐理解：

- 我到底在乎什么。
- 我最常被什么影响。
- 什么样的生活节奏更适合我。

