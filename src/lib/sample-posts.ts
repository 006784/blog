export function isRenderablePublicPost(post: Partial<PublicCatalogPost> | null | undefined): post is PublicCatalogPost {
  return Boolean(
    post &&
      post.status === 'published' &&
      typeof post.slug === 'string' &&
      post.slug.trim() &&
      typeof post.title === 'string' &&
      post.title.trim() &&
      typeof post.description === 'string' &&
      post.description.trim() &&
      typeof post.created_at === 'string' &&
      typeof post.updated_at === 'string'
  );
}

export function getSamplePosts(): PublicCatalogPost[] {
  return SAMPLE_ARTICLE_SOURCES
    .map(createSamplePost)
    .sort((a, b) => {
      const aPinned = a.is_pinned ? 1 : 0;
      const bPinned = b.is_pinned ? 1 : 0;
      if (aPinned !== bPinned) return bPinned - aPinned;
      return new Date(b.published_at || b.created_at).getTime() - new Date(a.published_at || a.created_at).getTime();
    });
}

export function getSamplePostBySlug(slug: string): PublicCatalogPost | null {
  return getSamplePosts().find((post) => post.slug === slug) || null;
}

